#!/usr/bin/env bash

dotfiles=$(pwd -P)
source $dotfiles/printf.sh

set -e

echo ''


link_file () {
  local src=$1 dst=$2

  local overwrite= backup= skip=
  local action=

  if [ -f "$dst" -o -d "$dst" -o -L "$dst" ]
  then

    if [ "$overwrite_all" == "false" ] && [ "$backup_all" == "false" ] && [ "$skip_all" == "false" ]
    then

      local currentSrc="$(readlink $dst)"

      if [ "$currentSrc" == "$src" ]
      then

        skip=true;

      else

        user "File already exists: $dst ($(basename "$src")), what do you want to do?\n\
        [s]kip, [S]kip all, [o]verwrite, [O]verwrite all, [b]ackup, [B]ackup all?"
        read -n 1 action

        case "$action" in
          o )
            overwrite=true;;
          O )
            overwrite_all=true;;
          b )
            backup=true;;
          B )
            backup_all=true;;
          s )
            skip=true;;
          S )
            skip_all=true;;
          * )
            ;;
        esac

      fi

    fi

    overwrite=${overwrite:-$overwrite_all}
    backup=${backup:-$backup_all}
    skip=${skip:-$skip_all}

    if [ "$overwrite" == "true" ]
    then
      rm -rf "$dst"
      success "removed $dst"
    fi

    if [ "$backup" == "true" ]
    then
      mv "$dst" "${dst}.backup"
      success "moved $dst to ${dst}.backup"
    fi

    if [ "$skip" == "true" ]
    then
      success "skipped $src"
    fi
  fi

  if [ "$skip" != "true" ]  # "false" or empty
  then
    ln -s "$1" "$2"
    success "linked $1 to $2"
  fi
}

SCRIPTS=$dotfiles/bin

info 'installing dotfiles'
bash $SCRIPTS/symlinks.sh
success "Done!"

info "Installing dependencies"
bash $SCRIPTS/homebrew-install.sh
bash $SCRIPTS/git-install.sh
bash $SCRIPTS/python-install.sh
bash $SCRIPTS/nodejs-install.sh
bash $SCRIPTS/zsh-install.sh
bash $SCRIPTS/macos/base.sh
bash $SCRIPTS/pcat-install.sh
success "Done!"

info 'Installing applications...'
sh -c applications.sh
success ' Done!'

info ' Making ~/Developer folder'
mkdir ~/Developer
success ' Done!'

success '  All installed!'